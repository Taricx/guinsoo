package org.guinsoo.java.lang;

/**
 * A java.lang.String implementation.
 */
public class Math {

    /**
     * Get the larger of both values.
     *
     * @param a the first value
     * @param b the second value
     * @return the larger
     */
    public static int max(int a, int b) {
        return a > b ? a : b;
    }

}
